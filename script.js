// Función para sumar dos números
function sumar(a, b) {
    return a + b;
}

// Función para restar dos números
function restar(a, b) {
    return a - b;
}

// Ejemplo de uso (por ahora solo se imprime en consola)
console.log("Ejemplo de la función sumar: 5 + 3 =", sumar(5, 3));
console.log("Ejemplo de la función restar: 10 - 4 =", restar(10, 4));